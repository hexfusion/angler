<p>Consider using spaces before and after your bad and goodwords.  For example, consider the word &quot;ass&quot; - sorry for the profanity!! :) </p>
<p>If you enter &quot;ass&quot; in the badword column to be replaced with the goodword of &quot;donkey&quot;, you may not get the results you are looking for.</p>
<p>&quot;You are very crass&quot; would appear as &quot;You are very crdonkey&quot; after the substitution. The solution? For the badword input, use this, &quot;&amp;nbsp;ass&amp;nbsp;&quot; and for the good word use &quot;&amp;nbsp;donkey&amp;nbsp;&quot;. Notice the space before and after the word. The spaces are achieved with this html code: &quot;&amp;nbsp;&quot;</p>
<p>You can also insert html code into your goodword list. You might want to make your badword substitutions in a red, bold font or something of that nature to alert readers of the review that a word substitution has taken place. In our example above, if you wanted the word &quot;donkey&quot; to appear as &quot;<span class="style1">donkey</span>&quot;, you would insert the following code:</p>
<p>&lt;b&gt;&lt;font color=red&gt;donkey&lt;/font&gt;&lt;/b&gt;</p>
